package com.pract.first;

public class Mainif {
public static void main(String[] args) {
	//if statement : performs a block of code if it's condition evaluates to be true
	int age = 18;
	if(age >= 18) {
		System.out.println("You are adult!");
	}else if(age < 18){// Else if is used to check more than one condition
		System.out.println("You are the kid ");
	}
	else {
		System.out.println("You are not adult");
	}
	//If the else statement will execute when the if condition is false 
}
}
